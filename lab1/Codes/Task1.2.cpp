//Take two integer inputs from the user and print their summation and multiplication.

#include <iostream>
using namespace std; 

int main() {
    
    int num1, num2;

    cout << "Enter first number- ";
    cin >> num1;

    cout << "Enter second number- ";
    cin >> num2;

    cout << "Summation: " << num1+num2 << endl;
    cout << "Multiplication: " << num1*num2;


    return 0;

}